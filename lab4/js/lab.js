// Get 2 willing players.
// Each player forms a fist with their hand underneath.
// Each player hits their fist against their palm three times.
// Mentally, each player silently decides whether to do a rock, paper, or scissors motion with their hand.
// Players then count to three and display their motion.
  // Hand beats rock
  // Scissors beats paper
  // Rock beats scissors
  // Paper beats rock
  // Scissors beats hand
// Players do a best of three, whoever wins two rounds or three in a row is the winner.
